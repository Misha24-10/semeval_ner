hidden_size = 512

class XLMRobertaBILSTMForTokenClassification(XLMRobertaPreTrainedModel):

    _keys_to_ignore_on_load_unexpected = [r"pooler"]
    _keys_to_ignore_on_load_missing = [r"position_ids"]

    def __init__(self, config):
        super().__init__(config)
        self.num_labels = config.num_labels

        self.roberta = XLMRobertaModel(config, add_pooling_layer=False)
        classifier_dropout = (
            config.classifier_dropout if config.classifier_dropout is not None else config.hidden_dropout_prob
        )
        self.dropout = nn.Dropout(classifier_dropout)
        self.bilstm = nn.LSTM(bidirectional=True, num_layers=NUM_LAYER, input_size=config.hidden_size, hidden_size=hidden_size)
        self.classifier = nn.Linear(2*hidden_size, self.num_labels)

    def init_hidden(self, batch_size):
        return torch.zeros(NUM_LAYER*2, batch_size, hidden_size).to("cuda"), torch.zeros(NUM_LAYER*2, batch_size, hidden_size).to("cuda")
    
        

        
